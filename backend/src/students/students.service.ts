import { BadRequestException, Injectable, NotFoundException } from '@nestjs/common';
import { InjectRepository } from '@nestjs/typeorm';
import { Student } from './entities/student.entity';
import { Repository, DataSource } from 'typeorm';
import { CreateStudentDto } from './dtos/create-student.dto';
import { UsersService } from 'src/users/users.service';
import { User } from 'src/users/entities/user.entity';
import { Department, EnrollmentStatus, Gender, Level, Role } from 'utils/enum';

@Injectable()
export class StudentsService {
  constructor(
    private readonly dataSource: DataSource,
    @InjectRepository(Student)
    private readonly studentRepository: Repository<Student>,
    @InjectRepository(User)
    private readonly userRepository: Repository<User>,
    private readonly usersService: UsersService,
  ) { }

  /**
   * Create a new student
   * @param dto {user: CreateUserDto, student: CreateStudentDto}
   * @returns {message: string, data: {user: User, student: Student}}
   */
  public async createStudent(dto: CreateStudentDto, admin_id: string) {
    await this.usersService.checkValidation(admin_id, Role.ADMIN);

    const { user, ...studentData } = dto;

    const existingUser = await this.userRepository.findOne({
      where: { national_id: user.national_id },
    });
    if (existingUser) {
      throw new BadRequestException('National ID already in use');
    }

    const existingStudent = await this.studentRepository.findOne({
      where: { student_code: studentData.student_code },
    });
    if (existingStudent) {
      throw new BadRequestException('Student code already in use');
    }

    return await this.dataSource.transaction(async (manager) => {
      const newUser = manager.create(User, {
        full_name: user.full_name,
        national_id: user.national_id,
        email: user.email,
        phone: user.phone,
        address: user.address,
        role: Role.STUDENT,
        password_hash: null,
        is_password_set: false,
        date_of_birth: new Date(user.date_of_birth),
        gender: user.gender,
      });

      const savedUser = await manager.save(newUser);

      const newStudent = manager.create(Student, {
        id: savedUser.id,
        user: savedUser,

        ...studentData,

        enrollment_date: new Date(studentData.enrollment_date),
      });

      const savedStudent = await manager.save(newStudent);

      return {
        message: 'Student created successfully',
        data: {
          user: savedUser,
          student: savedStudent,
        },
      };
    });
  }

  /**
   * Get all students
   * @returns {message: string, data: {students: Student[]}}
   */
  public async getAllStudents(
    level?: Level,
    department?: Department,
    gender?: Gender
  ) {

    let students = await this.studentRepository.find({
      relations: ['user'],
      where: {
        level,
        department,
      },
    });

    if (gender) {
      students = students.filter((student) => student.user.gender === gender);
    }

    return {
      message: 'Students fetched successfully',
      data: {
        students,
      },
    };
  }

  /**
   * Get student by code, Student code is unique and always generated by the system, 
   * and assigned to the student when created
   * @param student_code
   * @returns
   */
  public async getStudentByCode(student_code: string) {
    const student = await this.studentRepository.findOne({
      where: { student_code },
      relations: ['user', 'enrollments', 'enrollments.course'],
    });
    if (!student) {
      throw new BadRequestException('Student not found');
    }
    return student;
  }

  /**
   * Get student by id
   * @param id
   * @returns
   */
  public async getStudentById(id: string) {
    const student = await this.studentRepository.findOne({
      where: { id },
      relations: ['user', 'enrollments', 'enrollments.course'],
    });

    if (!student) {
      throw new NotFoundException('Student not found');
    }

    return student;
  }


  /**
   * Get student completed courses
   * @param student_id
   * @returns
   */
  public async getStudentCompletedCourses(student_id: string) {
    const student = await this.getStudentById(student_id);
    return student.enrollments.filter((enrollment) => enrollment.status === EnrollmentStatus.FINISHED).map((enrollment) => enrollment.course);
  }


  /**
   * Get student courses by status
   * @param student_id
   * @param course_status
   * @returns
   */
  public async getStudentCourses(student_id: string, course_status?: EnrollmentStatus) {
    const student = await this.getStudentById(student_id);
    const enrollments = student.enrollments
      .filter((enrollment) => course_status && enrollment.status === course_status)
      .map((enrollment) => enrollment.course);

    return {
      message: 'Student courses fetched successfully',
      data: {
        enrollments,
      },
    };
  }
}
